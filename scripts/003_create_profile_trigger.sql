-- Function to create profile on user signup
create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (id, name, email, role, is_active, classroom, year)
  values (
    new.id,
    coalesce(new.raw_user_meta_data ->> 'name', 'Novo UsuÃ¡rio'),
    new.email,
    coalesce(new.raw_user_meta_data ->> 'role', 'student'),
    true,
    coalesce(new.raw_user_meta_data ->> 'classroom', 'A'),
    coalesce((new.raw_user_meta_data ->> 'year')::integer, 6)
  )
  on conflict (id) do nothing;

  return new;
end;
$$;

-- Trigger to call the function
drop trigger if exists on_auth_user_created on auth.users;

create trigger on_auth_user_created
  after insert on auth.users
  for each row
  execute function public.handle_new_user();
